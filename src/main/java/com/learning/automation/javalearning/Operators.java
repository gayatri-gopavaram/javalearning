package com.learning.automation.javalearning;

public class Operators {
	
	public static void main(String[] args) {
		
		int a = 100;
		int b = 200;
		int Result = a+b;
		int d = b-a;
		int e = a*b;
		int f = b/a;
		int g = b % a;
		System.out.println("Addition value of 100 and 200 is :" +"  "+ Result); 
		System.out.println("Subtraction value of 200 and 100 is :" +"  "+ d);
		System.out.println("multiplication value of 100 and 200 is :" +"  "+ e);
		System.out.println("division value of 100 and 200 is :" +"  "+ f);
		System.out.println("modulo value of 100 and 200 is :" +"  "+ g);
	}

}
